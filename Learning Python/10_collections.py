# sequences -> string, list, tuple
# mappings -> dictionary
# sets  -> set, frozenset


# string - ordered, immutable sequences of characters
        # e.g., - "Hello world"

# list - ordered, mutable collections that can contain mixed data types
        # e.g., [7,10,15.3,18,22,'hello',True]

# tuple - ordered, immutable collections similar to lists
        #e.g., (1,'hello',3.14,False)

# dictionary - unordered(pre 3.7 version) / insertion-ordered (3.7+ version) key-value pairs
        #e.g., {'name':'Alice', 'age':25, 'city': 'London'}

# set - unordered, mutable collection of unique elements
        #e.g., {1,2,3,4}

# frozenset - immutable version of a set

 


    



